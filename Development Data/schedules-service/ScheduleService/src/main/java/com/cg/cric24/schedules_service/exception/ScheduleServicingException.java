package com.cg.cric24.schedules_service.exception;

public class ScheduleServicingException extends Exception{

	public ScheduleServicingException() {
		super();
	}

	public ScheduleServicingException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ScheduleServicingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScheduleServicingException(String message) {
		super(message);
	}

	public ScheduleServicingException(Throwable cause) {
		super(cause);
	}
	
	

}
